package ab45_assignments;

//Assignment 10: WAP to print 1 to +ve value that never stops


public class Assignment10_InfiniteLoop 
{

	    public static void main(String[] args) 
	    {
	        int i = 1;
	        
	        // Infinite loop using while loop
	        while (true) 
	        {
	            System.out.println(i);
	            i++; // Increment i by 1 each time
	        }	    
	    }


}
